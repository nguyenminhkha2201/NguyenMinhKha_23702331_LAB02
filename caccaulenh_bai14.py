import math

# Tính từng phần của biểu thức
phan1 = 32 ** 0.2
phan2 = (1 / 64) ** -0.25
phan3 = (8 / 27) ** (1 / 3)

# Tính giá trị biểu thức A
A = phan1 - phan2 + phan3

# In kết quả ra màn hình
print(f"Giá trị của biểu thức A là: {A:.4f}")
